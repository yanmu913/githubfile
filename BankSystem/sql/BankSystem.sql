DROP TABLE IF EXISTS transactionRecord;
CREATE TABLE transactionRecord(
	`tid` INT(4) PRIMARY KEY AUTO_INCREMENT,
	`tcardNo` VARCHAR(16) UNIQUE NOT NULL,
	`transactionDate` DATETIME NOT NULL,
	`transactionAmount` DECIMAL(8,2) NOT NULL,
	`tbalance` DECIMAL(8,2) NOT NULL,
	`transactionType` VARCHAR(5) NOT NULL COMMENT'存款、取款',
	`remark` VARCHAR(20)
